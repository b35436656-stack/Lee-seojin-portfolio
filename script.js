function isVisible(row) {
  return String(row.visible || "TRUE").trim().toUpperCase() !== "FALSE";
}

function sortRows(rows) {
  return [...rows].sort((a, b) => {
    const av = Number(a.sortOrder);
    const bv = Number(b.sortOrder);
    if (Number.isFinite(av) && Number.isFinite(bv)) return av - bv;
    if (Number.isFinite(av)) return -1;
    if (Number.isFinite(bv)) return 1;
    return 0;
  });
}

function escapeHtml(value) {
  return String(value ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function sanitizeUrl(value) {
  const url = String(value || "").trim();
  if (!url) return "";
  if (/^(https?:\/\/|mailto:|tel:|\.\/|\/|#)/i.test(url)) return url;
  if (/^[\w가-힣 ./()_-]+\.(pdf|png|jpg|jpeg|webp|svg)$/i.test(url)) return url;
  return "";
}

function formatText(value) {
  let html = escapeHtml(value).replace(/\r\n/g, "\n");
  html = html.replace(/\[link:([^\]]+)\]([\s\S]*?)\[\/link\]/g, (_, rawUrl, label) => {
    const url = sanitizeUrl(rawUrl);
    return url ? `<a href="${escapeHtml(url)}" target="_blank" rel="noopener">${label}</a>` : label;
  });
  html = html.replace(/\[color:(#[0-9a-fA-F]{3,8})\]([\s\S]*?)\[\/color\]/g, '<span style="color:$1">$2</span>');
  html = html.replace(/\[red\]([\s\S]*?)\[\/red\]/g, '<span class="text-red">$1</span>');
  html = html.replace(/\[blue\]([\s\S]*?)\[\/blue\]/g, '<span class="text-blue">$1</span>');
  html = html.replace(/\[green\]([\s\S]*?)\[\/green\]/g, '<span class="text-green">$1</span>');
  html = html.replace(/\*\*([^*\n][\s\S]*?)\*\*/g, "<strong>$1</strong>");
  html = html.replace(/__([^_\n][\s\S]*?)__/g, "<u>$1</u>");
  html = html.replace(/(^|[^*])\*([^*\n]+)\*/g, "$1<em>$2</em>");
  return html.replace(/\n/g, "<br>");
}

function splitList(value) {
  return String(value || "")
    .split(/\n|,/)
    .map((item) => item.trim())
    .filter(Boolean);
}

function driveUrl(url, type = "file") {
  const value = sanitizeUrl(url);
  if (!value) return "";
  const match = value.match(/\/d\/([a-zA-Z0-9_-]+)/) || value.match(/[?&]id=([a-zA-Z0-9_-]+)/);
  if (!match) return value;
  const id = match[1];
  if (type === "image") return `https://drive.google.com/thumbnail?id=${id}&sz=w1400`;
  return `https://drive.google.com/file/d/${id}/view`;
}

function safeImageUrl(url, fallback) {
  return driveUrl(url, "image") || driveUrl(fallback, "image") || "assets/operations-visual.svg";
}

function validColor(value) {
  const color = String(value || "").trim();
  return /^#([0-9a-fA-F]{3}|[0-9a-fA-F]{6}|[0-9a-fA-F]{8})$/.test(color) ? color : "";
}

function validSize(value, fallback) {
  const size = String(value || "").trim();
  if (/^\d{1,2}(\.\d)?(px|rem)$/.test(size)) return size;
  return fallback;
}

function applyTheme(settings) {
  const themeName = THEME_PRESETS[settings.siteTheme] ? settings.siteTheme : "Light";
  const root = document.documentElement;
  Object.entries(THEME_PRESETS[themeName]).forEach(([key, value]) => root.style.setProperty(key, value));

  Object.entries(CSS_SETTING_MAP).forEach(([settingKey, cssVar]) => {
    const color = validColor(settings[settingKey]);
    if (color) root.style.setProperty(cssVar, color);
  });

  Object.entries(FONT_SETTING_MAP).forEach(([settingKey, cssVar]) => {
    const fallback = getComputedStyle(root).getPropertyValue(cssVar).trim() || "";
    const size = validSize(settings[settingKey], fallback);
    if (size) root.style.setProperty(cssVar, size);
  });

  const bg = driveUrl(settings.backgroundImageUrl, "image");
  root.style.setProperty("--hero-bg-image", bg ? `url("${bg}")` : "none");
}

function setFormatted(element, value) {
  if (!element || value === undefined || value === null || value === "") return;
  element.innerHTML = formatText(value);
}

function setPlain(element, value) {
  if (!element || value === undefined || value === null || value === "") return;
  element.textContent = value;
}

function applyGlobalText(settings) {
  document.title = settings.siteTitle || CMS_DEFAULTS.settings.siteTitle;
  setPlain(document.querySelector("[data-cms='siteName']"), settings.siteName || settings.personName);
  setPlain(document.querySelector("[data-cms='brandInitials']"), initials(settings.personName));

  document.querySelectorAll("[data-cms]").forEach((element) => {
    const key = element.getAttribute("data-cms");
    if (["siteName", "brandInitials"].includes(key)) return;
    setFormatted(element, settings[key]);
  });

  document.querySelectorAll("[data-cms-link]").forEach((element) => {
    const key = element.getAttribute("data-cms-link");
    const value = driveUrl(settings[key]);
    if (value) {
      element.href = value;
      element.classList.remove("is-disabled");
      element.removeAttribute("aria-disabled");
    } else {
      element.removeAttribute("href");
      element.classList.add("is-disabled");
      element.setAttribute("aria-disabled", "true");
      element.title = "Google Sheets에 PDF 링크를 입력하면 활성화됩니다.";
    }
  });

  document.querySelectorAll("[data-cms-image]").forEach((element) => {
    const key = element.getAttribute("data-cms-image");
    element.src = safeImageUrl(settings[key], settings.fallbackImageUrl);
  });
}

