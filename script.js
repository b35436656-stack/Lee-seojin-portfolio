function renderTags(value) {
  return splitList(value).map((tag) => `<span class="tag">${formatText(tag)}</span>`).join("");
}

function renderList(value, className = "role-list") {
  const items = splitList(value).map((item) => `<li>${formatText(item)}</li>`).join("");
  return items ? `<ul class="${className}">${items}</ul>` : "";
}

